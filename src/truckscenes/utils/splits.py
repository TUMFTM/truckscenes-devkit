def create_splits_logs(split: str, trucksc: TruckScenes) -> List[str]:
    """
    Returns the logs in each dataset split of TruckScenes.
    Note: Previously this script included the teaser dataset splits.
    Since new scenes from those logs were added and others removed in the full dataset,
    that code is incompatible and was removed.

    :param split: TruckScenes split.
    :param trucksc: TruckScenes instance.
    :return: A list of logs in that split.
    """
    # Load splits on a scene-level.
    scene_splits = create_splits_scenes(verbose=False)

    assert split in scene_splits.keys(), \
        'Requested split {} which is not a known TruckScenes split.'.format(split)

    # Check compatibility of split with trucksc_version.
    version = trucksc.version
    if split in {'train', 'val', 'train_detect', 'train_track'}:
        assert version.endswith('trainval'), \
            f'Requested split {split} which is not compatible with TruckScenes version {version}'
    elif split in {'mini_train', 'mini_val'}:
        assert version.endswith('mini'), \
            f'Requested split {split} which is not compatible with TruckScenes version {version}'
    elif split == 'test':
        assert version.endswith('test'), \
            f'Requested split {split} which is not compatible with TruckScenes version {version}'
    else:
        raise ValueError(f'Requested split {split} which this function cannot map to logs.')

    # Get logs for this split.
    scene_to_log = {
        scene['name']: trucksc.get('log', scene['log_token'])['logfile'] for scene in trucksc.scene
    }
    logs = set()
    scenes = scene_splits[split]
    for scene in scenes:
        logs.add(scene_to_log[scene])

    return list(logs)


def create_splits_scenes(verbose: bool = False) -> Dict[str, List[str]]:
    """
    Similar to create_splits_logs, but returns a mapping from split to scene names,
    rather than log names.

    The splits are as follows:
    - train/val/test: The standard splits of the TruckScenes dataset (523/75/149 scenes).
    - mini_train/mini_val: Train and val splits of the mini subset used for visualization
        and debugging (8/2 scenes).
    - train_detect/train_track: Two halves of the train split used for separating the
        training sets of detector and tracker if required.

    :param verbose: Whether to print out statistics on a scene level.
    :return: A mapping from split name to a list of scenes names in that split.
    """
    # Use hard-coded splits.
    all_scenes = train + val + test
    assert len(all_scenes) == 747 and len(set(all_scenes)) == 747, 'Error: Splits incomplete!'
    scene_splits = {'train': train, 'val': val, 'test': test,
                    'mini_train': mini_train, 'mini_val': mini_val,
                    'train_detect': train_detect, 'train_track': train_track}

    # Optional: Print scene-level stats.
    if verbose:
        for split, scenes in scene_splits.items():
            print('%s: %d' % (split, len(scenes)))
            print('%s' % scenes)

    return scene_splits


